<h2>Description</h2>

<p>The purpose of HyperLivre is to allow users to contribute on writing short notes, or short pieces of knowledge. This is not quite a blog, not a CMS, nor a concurrent editing application. HyperLivre should remain simple, without fancy staff build into it. It still borrows similar common notions from those platforms, and can be seen in some way as a lightweight collaborative blog or wiki.</p>

<p>Pieces of knowledge, called notes, must stay concise and precise, going straight to the point. They may contain interesting pieces of work (code for instance), provide real and working examples, or explore a general concept. At a first glance, HyperLivre looks like a blog. But, notes can be written by groups of people, their material need to be structured in some way, and their subjects bound to pre-created concepts.</p>

<p>Searchers in the language field were interested to try an alternative writing method. Using short notes to progress, they wanted to guide and assess the knowledge built in this way. The HyperLivre project was initially created for this purpose. With semantic hyperlinks there is a way to organize ideas, or more helpfully some concepts emerge in the graph as central ideas. By electing a supervisor user which can edit concepts, it is then possible to guide knowledge exploration.</p>

<h2>Actual Status</h2>

<p>The initial HyperLivre project, needed to be massively refactored. As developers, we wanted to try the young FuelPHP framework which looks really awesome, and give it a good try. We recycled some of the original requirements and added some we found interesting to have. The initial HyperLivre relied also on an other project (Lazy) which was used on the backend. Something we wanted HyperLivre to possess on its own.</p>

<p>The refactoring main purposes were to achieve better usability, portability and modularity. We wanted to keep things as simple and clean as possible on both ends, and still improve the frontend user experience.</p>

<p>This brand new project is actually under active development. Right now, only a few basic functionalities are available, compared to the initial HyperLivre project. But, stay tuned !</p>

